		/** Information about an image. */
		struct ImageMetadata
		{
			/**
			 * @brief
			 * ImageMetadata constructor.
			 *
			 * @param width
			 * Width of image.
			 * @param height
			 * Height of image.
			 * @param ppi
			 * Resolution of image in pixels per inch.
			 * @param ct
			 * Technology used to capture the image.
			 * @param o
			 * Hand orientation depicted in the image.
			 */
			ImageMetadata(
			    const uint16_t width,
			    const uint16_t height,
			    const uint16_t ppi,
			    const SlapImage::CaptureTechnology ct,
			    const SlapImage::Orientation o) :
			    width(width),
			    height(height),
			    ppi(ppi),
			    captureTechnology(ct),
			    orientation(o) {}

			/** Width of image. */
			const uint16_t width{};
			/** Height of image. */
			const uint16_t height{};
			/** Resolution of image in pixels per inch. */
			const uint16_t ppi{};
			/** Technology used to capture the image. */
			const SlapImage::CaptureTechnology captureTechnology{};
			/** Hand orientation depicted in the image. */
			const SlapImage::Orientation orientation{};
		};
